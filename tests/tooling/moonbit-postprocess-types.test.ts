import assert from "node:assert/strict";
import fs from "node:fs";
import path from "node:path";
import { test } from "node:test";

import { repoRoot, runMoonScript } from "./_helpers/moonbit.ts";

const scratchRoot = path.join(repoRoot, "target", "vize-tests", "tooling-tests");

function createFixtureRoot(): string {
  fs.mkdirSync(scratchRoot, { recursive: true });
  const root = fs.mkdtempSync(path.join(scratchRoot, "postprocess-types-"));
  fs.mkdirSync(path.join(root, "npm", "cli", "src", "types"), { recursive: true });
  return root;
}

function generatedTypesPath(root: string): string {
  return path.join(root, "npm", "cli", "src", "types", "generated.ts");
}

test("postprocess_types script applies generated config type patches", () => {
  const root = createFixtureRoot();
  const generatedPath = generatedTypesPath(root);
  fs.writeFileSync(
    generatedPath,
    `/* eslint-disable */
/**
 * This file was automatically generated by json-schema-to-typescript.
 * DO NOT MODIFY IT BY HAND. Instead, modify the source JSONSchema file,
 * and run json-schema-to-typescript to regenerate this file.
 */

export interface VizeConfig {
  /**
   * JSON Schema reference for editor autocompletion
   */
  $schema?: string;
  musea?: MuseaA11YConfig;
}

export interface VitePluginConfig {
  include?: string | string[];
  exclude?: string | string[];
}

export interface MuseaA11YConfig {}
`,
  );

  const result = runMoonScript("postprocess_types", [], {
    env: {
      VIZE_REPO_ROOT: root,
    },
  });
  assert.equal(result.status, 0, `${result.stderr}\n${result.stdout}`.trim());

  const expected = `/* eslint-disable */
/**
 * This file was automatically generated by json-schema-to-typescript.
 * DO NOT MODIFY IT BY HAND. Instead, modify the source JSONSchema file,
 * and run json-schema-to-typescript to regenerate this file.
 */

export type LintPreset = "happy-path" | "opinionated" | "essential" | "incremental" | "ecosystem" | "nuxt";

export type RuleSeverity = "off" | "warn" | "error";

export type RuleCategory = "correctness" | "suspicious" | "style" | "perf" | "a11y" | "security";

export interface VizeConfig {
  musea?: MuseaA11yConfig;
}

export interface VitePluginConfig {
  include?: string | RegExp | (string | RegExp)[];
  exclude?: string | RegExp | (string | RegExp)[];
}

export interface MuseaA11yConfig {}
`;

  assert.equal(fs.readFileSync(generatedPath, "utf8"), expected);

  const secondResult = runMoonScript("postprocess_types", [], {
    env: {
      VIZE_REPO_ROOT: root,
    },
  });
  assert.equal(secondResult.status, 0, `${secondResult.stderr}\n${secondResult.stdout}`.trim());
  assert.equal(fs.readFileSync(generatedPath, "utf8"), expected);
});
