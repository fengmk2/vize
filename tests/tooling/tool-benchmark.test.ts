test("tool benchmark markdown is suitable for PR comments", () => {
  const markdown = renderMarkdown(data);

  assert.match(markdown, /## Tool Benchmark/);
  assert.match(markdown, /blacksmith-32vcpu-ubuntu-2404/);
  assert.match(markdown, /32 vCPU \/ 128 GB RAM \/ 1\.5 TB storage/);
  assert.match(markdown, /SFC compile/);
  assert.match(markdown, /30\.0x/);
  assert.match(markdown, /Measured: 2026-06-01T00:00:00\.000Z/);
  assert.match(markdown, /Large SFC: 900 repeated template blocks/);
  assert.match(markdown, /Commands:/);
  assert.match(markdown, /Fairness notes:/);
  assert.match(markdown, /Variant details and raw run times/);
});

test("tool benchmark document wraps the generated summary for docs", () => {
  const document = renderDocument(data);

  assert.match(document, /title: Blacksmith Benchmark Snapshot/);
  assert.match(document, /Generated by \.github\/workflows\/tool-benchmark\.yml/);
  assert.match(document, /## Latest Result/);
  assert.doesNotMatch(document, /^## Tool Benchmark/m);
});
